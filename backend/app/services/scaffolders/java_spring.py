def _readme(spec: ScaffoldSpec) -> str:
    return f"""# {spec.project_name}

> Scaffold inicial gerado pelo **GCA**. Edite normalmente — apenas
> arquivos com cabeçalho `[gca:auto]` podem ser sobrescritos em
> regenerações futuras.

## Stack

- Java {spec.java_version}
- Spring Boot {spec.framework_version or _SPRING_BOOT_DEFAULT_VERSION}
- Maven
{f"- {spec.database}" if spec.database else ""}
{"- Redis" if spec.requires_redis else ""}
{"- Spring Security (auth/RBAC)" if spec.requires_security else ""}

## Como rodar

```bash
./mvnw spring-boot:run
# ou
mvn spring-boot:run
```

A aplicação sobe em `http://localhost:8080`.
Health check: `http://localhost:8080/actuator/health`.

## Testes

```bash
./mvnw test
```

## Variáveis de ambiente

Veja `src/main/resources/application.yml` para os defaults. Configure
`DATABASE_URL`, `DATABASE_USER`, `DATABASE_PASSWORD`, `REDIS_HOST`,
etc no ambiente de execução.
"""


def scaffold_java_spring(spec: ScaffoldSpec) -> List[ScaffoldFile]:
    """Gera a estrutura inicial determinística de um projeto Spring Boot.

    Não chama LLM. Idempotente — chamar duas vezes com o mesmo `spec`
    produz a mesma lista de arquivos com o mesmo conteúdo.
    """
    pkg_path = _package_to_path(spec.package)
    files: List[ScaffoldFile] = [
        ScaffoldFile("pom.xml", _pom_xml(spec)),
        ScaffoldFile(".gitignore", _gitignore()),
        ScaffoldFile("README.md", _readme(spec)),
        ScaffoldFile(
            f"src/main/java/{pkg_path}/{_class_name_from_slug(spec.project_slug)}Application.java",
            _application_java(spec),
        ),
        ScaffoldFile(
            "src/main/resources/application.yml",
            _application_yml(spec),
        ),
        ScaffoldFile(
            f"src/test/java/{pkg_path}/{_class_name_from_slug(spec.project_slug)}ApplicationTests.java",
            _application_tests_java(spec),
        ),
    ]
    if spec.requires_security:
        files.append(
            ScaffoldFile(
                f"src/main/java/{pkg_path}/config/{_class_name_from_slug(spec.project_slug)}SecurityConfig.java",
                _security_config_java(spec),
            )
        )
    return files
